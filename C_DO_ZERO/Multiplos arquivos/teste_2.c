// Implementação da função 'soma'. Ela recebe dois inteiros 'a' e 'b' e retorna a soma deles.
int soma(int a, int b) {
    return a + b;
}

// Implementação da função 'Mult'. Ela recebe dois inteiros 'a' e 'b' e retorna a multiplicação deles.
int Mult(int a, int b) {
    return a * b;
}

// Implementação da função 'Sub'. Ela recebe dois inteiros 'a' e 'b' e retorna a subtração de 'a' por 'b'.
int Sub(int a, int b) {
    return a - b;
}
